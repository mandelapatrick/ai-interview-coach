import { Question, PMQuestionType } from "@/types";

// Helper to normalize question types from spreadsheet
function normalizeType(rawType: string): PMQuestionType {
  const lower = rawType.toLowerCase();
  if (lower.includes("product sense") || lower.includes("product design") || lower.includes("design") || lower === "product") {
    return "product-sense";
  }
  if (lower.includes("behavioral") || lower.includes("behavior") || lower.includes("leadership")) {
    return "behavioral";
  }
  if (lower.includes("technical") || lower.includes("system design")) {
    return "technical";
  }
  if (lower.includes("execution") || lower.includes("metrics") || lower.includes("analytics") || lower.includes("a/b")) {
    return "execution";
  }
  if (lower.includes("estimation")) {
    return "estimation";
  }
  if (lower.includes("strategy")) {
    return "strategy";
  }
  return "product-sense"; // default
}

