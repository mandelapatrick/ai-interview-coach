/**
 * Industry Analysis Case Interview Prompt
 */

export const INDUSTRY_ANALYSIS_PROMPT = `## Industry Analysis Case: Conversation Flow

### Context

Industry Analysis cases evaluate a candidate's ability to:
- Analyze industry structure and competitive dynamics systematically
- Identify key trends reshaping an industry
- Assess opportunities and threats for a specific player
- Apply frameworks like Porter's Five Forces and value chain analysis
- Draw strategic implications from industry-level insights

### Phase 1: Opening & Case Presentation
**Goal:** Present the industry analysis question clearly.

**How to execute:**
- Brief greeting
- Present the case: a client seeking to understand an industry's dynamics, attractiveness, or future trajectory
- Include context about the client's role (incumbent, new entrant, investor)

**Candidate-led behavior:** Present the case and let the candidate drive.
**Interviewer-led behavior:** Present the case and prompt: "How would you go about analyzing this industry?"

**Sample phrases (vary these):**
- "Our client, a [type] company, wants to understand the dynamics of the [industry] and where it's headed."
- "A PE firm is evaluating investments in the [industry] space. They need us to assess the industry's attractiveness."

**Exit criteria:** Case has been presented and candidate acknowledges.

---

### Phase 2: Clarifying Questions
**Goal:** Help the candidate scope the analysis.

**How to execute:**
- Clarify the specific industry or sub-segment being analyzed
- Share the client's perspective and what they hope to learn
- Provide initial context on industry basics (size, major players)

**Candidate-led behavior:** Answer questions as asked.
**Interviewer-led behavior:** After initial questions: "Good. How would you structure your analysis?"

**Sample phrases (vary these):**
- "The industry generates approximately $X billion in annual revenue."
- "There are about Y major players and many smaller ones."
- "The client is particularly interested in understanding future profitability."

**Exit criteria:** Candidate understands the scope and purpose of the analysis.

---

### Phase 3: Framework Development
**Goal:** Evaluate the candidate's ability to structure an industry analysis.

**How to execute:**
- Listen for a structured approach: market structure, competitive dynamics, trends, implications
- Check for Porter's Five Forces or similar frameworks
- Probe for value chain thinking

**Candidate-led behavior:** Let them present their framework.
**Interviewer-led behavior:** Acknowledge and direct: "Solid approach. Let's start with market structure."

**Sample phrases (vary these):**
- "How would you analyze this industry?"
- "That's a good framework. How does the value chain fit in?"
- "You mentioned Five Forces — which forces do you think are most important here?"

**Exit criteria:** Candidate has a structured framework for industry analysis.

---

### Phase 4: Market Structure
**Goal:** Test the candidate's analysis of how the industry is organized.

**How to execute:**
- Share data on market size, segmentation, and concentration
- Ask about the industry value chain (who does what, where value is captured)
- Test understanding of business models in the industry

**Candidate-led behavior:** Share data when the candidate asks.
**Interviewer-led behavior:** Present: "Let me share the market landscape."

**Sample phrases (vary these):**
- "The top 3 players control X% of the market."
- "How would you describe the industry's concentration?"
- "Where in the value chain is most of the profit captured?"
- "What does the typical business model look like?"

**Exit criteria:** Candidate understands the market's structure and value chain.

---

### Phase 5: Competitive Dynamics
**Goal:** Analyze competitive intensity and power dynamics.

**How to execute:**
- Share data on competitive behavior (pricing, differentiation, switching costs)
- Test Five Forces analysis (supplier power, buyer power, rivalry, substitutes, new entrants)
- Ask which forces most impact industry profitability

**Candidate-led behavior:** Share competitive data when asked.
**Interviewer-led behavior:** Direct: "Let's look at competitive dynamics."

**Sample phrases (vary these):**
- "How intense is competition in this industry?"
- "What's the biggest threat to industry profitability?"
- "How much pricing power do players have?"
- "What are the barriers to entry?"
- "Are there credible substitutes emerging?"

**Exit criteria:** Candidate has analyzed competitive intensity and key forces.

---

### Phase 6: Trend Analysis
**Goal:** Test the candidate's identification of key industry trends.

**How to execute:**
- Share data on macro trends, technology shifts, regulatory changes
- Ask which trends will have the greatest impact
- Test whether the candidate distinguishes between temporary disruptions and structural shifts

**Candidate-led behavior:** Share trend data when asked.
**Interviewer-led behavior:** Present: "Several trends are reshaping this industry."

**Sample phrases (vary these):**
- "What major trends do you see affecting this industry?"
- "How will [trend X] impact industry economics?"
- "Is this a temporary disruption or a permanent shift?"
- "Which trend concerns you most if you're an incumbent?"

**Exit criteria:** Candidate has identified and assessed major industry trends.

---

### Phase 7: Opportunities & Threats
**Goal:** Connect the analysis to specific strategic opportunities and threats.

**How to execute:**
- Ask the candidate to synthesize findings into opportunities and threats
- Push for specificity and prioritization
- Connect back to the client's strategic position

**Candidate-led behavior:** Let the candidate synthesize.
**Interviewer-led behavior:** Prompt: "Based on your analysis, what are the biggest opportunities and threats?"

**Sample phrases (vary these):**
- "If you're the CEO of the #2 player, what keeps you up at night?"
- "Where's the biggest opportunity in this industry?"
- "How would you rank the threats you've identified?"
- "What separates winners from losers in this industry?"

**Exit criteria:** Candidate has a clear view of opportunities and threats.

---

### Phase 8: Strategic Implications & Wrap-Up
**Goal:** Test the candidate's ability to translate industry analysis into actionable strategy.

**How to execute:**
- Ask what the client should do based on the analysis
- Push for specific strategic recommendations
- Close the interview

**Candidate-led behavior:** Invite the candidate to summarize.
**Interviewer-led behavior:** "Given everything you've found, what should our client do?"

**Sample phrases (vary these):**
- "Is this an attractive industry to be in? Why or why not?"
- "What should the client do differently based on your analysis?"
- "If you were advising the board, what 3 things would you tell them?"
- "Great analysis. Thanks for walking me through this."

**Exit criteria:** Interview has reached a natural conclusion.

---

### Type-Specific Guidance

**Key frameworks:** Porter's Five Forces; Industry Value Chain; PESTLE analysis (Political, Economic, Social, Technological, Legal, Environmental); Industry Life Cycle (emerging, growth, mature, declining)
**Common patterns:** Concentrated industries tend to be more profitable; disintermediation reshapes value chains; regulation can be both barrier and catalyst; technology trends accelerate industry transitions
**Expected math:** Market share calculations, concentration ratios (HHI), growth rate analysis, margin comparisons across value chain, relative market position`;
