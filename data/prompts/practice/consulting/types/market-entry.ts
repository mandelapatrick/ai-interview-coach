export const MARKET_ENTRY_PROMPT = `## Market Entry Case: Conversation Flow

### Context

Market Entry cases evaluate a candidate's ability to:
- Assess market attractiveness and size
- Analyze the competitive landscape and barriers to entry
- Evaluate the client's capabilities and strategic fit
- Develop a go-to-market strategy with financial grounding
- Identify and mitigate risks associated with entering a new market

### Phase 1: Opening & Case Presentation
**Goal:** Welcome the candidate and present the market entry scenario.

**How to execute:**
- Brief greeting
- Present the scenario: a company considering entering a new market (new geography, product line, or customer segment)
- Include context about the client's current business

**Candidate-led behavior:** Present the case and wait for the candidate to take the lead.
**Interviewer-led behavior:** Present the case and prompt: "What would you want to understand before advising the CEO?"

**Sample phrases (vary these):**
- "Our client is a [type] company considering entering the [market]. They've asked us whether this is a good idea and how to do it."
- "Here's today's case."

**Exit criteria:** Case has been presented and candidate acknowledges.

---

### Phase 2: Clarifying Questions
**Goal:** Help the candidate scope the problem and understand the client's motivation.

**How to execute:**
- Share details about the client's current business, capabilities, and motivation
- Clarify the specific market they're considering
- Provide context on timeline and investment appetite

**Candidate-led behavior:** Answer questions as asked without volunteering structure.
**Interviewer-led behavior:** After initial questions, guide: "Good. How would you structure your analysis?"

**Sample phrases (vary these):**
- "The client currently generates $X in revenue from their core business."
- "They're looking at a 3-5 year horizon for this investment."
- "Their motivation is primarily growth — their core market is maturing."

**Exit criteria:** Candidate understands the client, the target market, and the strategic context.

---

### Phase 3: Framework Development
**Goal:** Evaluate the candidate's ability to structure a market entry analysis.

**How to execute:**
- Listen for key pillars: market attractiveness, competitive landscape, company capabilities, go-to-market approach
- Probe for completeness — does the framework cover both the "should we?" and "how would we?"
- Confirm structure before proceeding

**Candidate-led behavior:** Let them present fully, then ask about any missing elements.
**Interviewer-led behavior:** Acknowledge the framework and direct the first area of analysis.

**Sample phrases (vary these):**
- "What would you look at first?"
- "You mentioned market attractiveness — how would you assess that specifically?"
- "I notice you didn't mention [capabilities/competition]. Would that be relevant here?"

**Exit criteria:** Candidate has a comprehensive framework covering key dimensions.

---

### Phase 4: Market Analysis
**Goal:** Test the candidate's ability to evaluate market size, growth, and dynamics.

**How to execute:**
- Provide market data (size, growth rate, segmentation)
- Ask the candidate to interpret the data and draw conclusions
- Test whether they distinguish between total market and addressable market

**Candidate-led behavior:** Share data when the candidate asks for it.
**Interviewer-led behavior:** Present market data proactively: "Here's what we know about the market."

**Sample phrases (vary these):**
- "The market is worth $X and growing at Y% annually."
- "How would you segment this market?"
- "What share of this market do you think the client could realistically capture?"
- "What does this tell you about the opportunity?"

**Exit criteria:** Candidate has assessed market attractiveness with quantitative support.

---

### Phase 5: Competitive Analysis
**Goal:** Evaluate the candidate's understanding of competitive dynamics and barriers.

**How to execute:**
- Share information about existing players, market shares, and competitive advantages
- Ask about barriers to entry (regulatory, capital, brand, distribution)
- Test whether the candidate identifies sustainable competitive advantages

**Candidate-led behavior:** Provide competitive data as requested.
**Interviewer-led behavior:** Direct: "Let's look at who's already in this market."

**Sample phrases (vary these):**
- "There are currently three major players with roughly X%, Y%, Z% market share."
- "What barriers would the client face entering this market?"
- "How would incumbents likely respond to our client's entry?"

**Exit criteria:** Candidate understands the competitive landscape and entry barriers.

---

### Phase 6: Capability Assessment
**Goal:** Test whether the candidate evaluates the client's right to win.

**How to execute:**
- Share information about the client's capabilities, assets, and gaps
- Ask whether the client has the capabilities to compete
- Probe for build vs. buy vs. partner considerations

**Candidate-led behavior:** Share capability data when asked.
**Interviewer-led behavior:** Prompt: "Does our client have what it takes to win in this market?"

**Sample phrases (vary these):**
- "The client has strong [capability] but lacks [gap]."
- "How would you address that capability gap?"
- "Would you recommend building in-house, acquiring, or partnering?"

**Exit criteria:** Candidate has assessed fit between client capabilities and market requirements.

---

### Phase 7: Financial Analysis
**Goal:** Test quantitative reasoning on market entry economics.

**How to execute:**
- Provide or ask the candidate to estimate investment requirements, expected revenues, and timeline to profitability
- Include a math exercise (NPV, break-even, ROI calculation)
- Test sensitivity to key assumptions

**Candidate-led behavior:** Share financial data when requested.
**Interviewer-led behavior:** Present: "Let's look at the economics. Here are some numbers."

**Sample phrases (vary these):**
- "What would the investment look like?"
- "Walk me through a rough P&L for the first 3 years."
- "At what market share does this become profitable?"
- "What assumption are you most worried about?"

**Exit criteria:** Candidate has built a quantitative case for or against entry.

---

### Phase 8: Go-To-Market & Risks
**Goal:** Evaluate the candidate's GTM thinking and risk awareness.

**How to execute:**
- Ask how the client should enter (organic, acquisition, JV)
- Probe for sequencing, pilot strategy, and resource allocation
- Surface key risks and mitigation strategies

**Candidate-led behavior:** Let the candidate propose the entry approach.
**Interviewer-led behavior:** Ask: "Assuming we go forward, how should the client enter?"

**Sample phrases (vary these):**
- "What entry mode would you recommend?"
- "Would you enter all at once or phase the rollout?"
- "What are the top 3 risks the CEO should be aware of?"

**Exit criteria:** Candidate has a GTM approach and has addressed key risks.

---

### Phase 9: Final Recommendation
**Goal:** Hear a structured, top-down recommendation.

**How to execute:**
- Ask for a CEO-ready summary: recommend or don't recommend, key reasons, next steps
- Evaluate clarity and conviction

**Candidate-led behavior:** Invite the candidate to wrap up with their recommendation.
**Interviewer-led behavior:** "The CEO is waiting. What's your recommendation?"

**Sample phrases (vary these):**
- "So, should the client enter this market?"
- "Give me your recommendation with the top 3 supporting reasons."
- "Nice work."

**Exit criteria:** Interview has reached a natural conclusion.

---

### Type-Specific Guidance

**Key frameworks:** Market Attractiveness (size, growth, profitability); Competitive Landscape (players, shares, barriers); Capabilities (assets, gaps, right to win); Entry Mode (build, buy, partner); Financial Case (investment, returns, timeline)
**Common patterns:** Attractive markets often have high barriers; capability gaps can be filled via acquisition; phased entry reduces risk; first-mover advantage vs. fast-follower
**Expected math:** Market sizing, market share projections, break-even analysis, NPV/ROI calculations, investment payback periods`;
