/**
 * M&A Case Interview Prompt
 */

export const M_AND_A_PROMPT = `## M&A Case: Conversation Flow

### Context

M&A cases evaluate a candidate's ability to:
- Assess whether an acquisition makes strategic sense
- Evaluate standalone value and synergy potential
- Analyze financial considerations (valuation, deal structure, returns)
- Identify integration risks and cultural fit issues
- Make a clear go/no-go recommendation with supporting logic

### Phase 1: Opening & Case Presentation
**Goal:** Present the M&A scenario clearly.

**How to execute:**
- Brief greeting
- Present the case: a company considering acquiring a target
- Include context about the acquirer, the target, and the strategic rationale

**Candidate-led behavior:** Present the case and let the candidate drive.
**Interviewer-led behavior:** Present the case and prompt: "What's your reaction to this deal? What would you want to understand?"

**Sample phrases (vary these):**
- "Our client, a [type] company, is considering acquiring [target]. They've asked us to evaluate whether this is a good deal."
- "Let me walk you through a potential acquisition our client is exploring."

**Exit criteria:** Case has been presented and candidate acknowledges.

---

### Phase 2: Clarifying Questions
**Goal:** Help the candidate understand the deal context.

**How to execute:**
- Share details about both companies (size, products, markets)
- Clarify the strategic motivation (growth, capabilities, market access, cost synergies)
- Provide deal context (price range, competitive bidders)

**Candidate-led behavior:** Answer questions as asked.
**Interviewer-led behavior:** After initial questions: "Good questions. How would you structure your evaluation?"

**Sample phrases (vary these):**
- "The acquirer is a $X revenue company in [industry]."
- "The target generates $Y in revenue with Z% margins."
- "The primary motivation is [strategic rationale]."

**Exit criteria:** Candidate understands both companies and the deal context.

---

### Phase 3: Framework Development
**Goal:** Evaluate the candidate's ability to structure an M&A analysis.

**How to execute:**
- Listen for key pillars: strategic fit, standalone valuation, synergies, risks
- Probe for completeness
- Confirm structure before proceeding

**Candidate-led behavior:** Let them present their framework fully.
**Interviewer-led behavior:** Acknowledge and direct: "Good structure. Let's start with strategic fit."

**Sample phrases (vary these):**
- "How would you think about evaluating this deal?"
- "That's a thorough framework. Where would you start?"
- "You mentioned synergies — can you be more specific about what types you'd look for?"

**Exit criteria:** Candidate has a structured approach to evaluating the acquisition.

---

### Phase 4: Strategic Fit Analysis
**Goal:** Test the candidate's assessment of why the deal makes strategic sense.

**How to execute:**
- Share information about strategic complementarities
- Ask about the acquirer's strategy and how the target fits
- Probe whether the candidate considers alternatives to acquisition

**Candidate-led behavior:** Share strategic data when requested.
**Interviewer-led behavior:** Present strategic context and ask for the candidate's assessment.

**Sample phrases (vary these):**
- "The target has strong capabilities in [area] where our client is weak."
- "Why would the client buy rather than build this capability?"
- "Are there other ways to achieve the same strategic objective?"
- "How does this acquisition fit the client's overall strategy?"

**Exit criteria:** Candidate has assessed strategic rationale for the deal.

---

### Phase 5: Valuation & Financials
**Goal:** Test quantitative analysis of deal economics.

**How to execute:**
- Provide financial data for both companies
- Ask the candidate to assess whether the price is reasonable
- Include a math exercise (multiple-based valuation, DCF-lite, or accretion/dilution)

**Candidate-led behavior:** Share financial data when requested.
**Interviewer-led behavior:** Present financials: "Here are the key numbers. What do they tell you?"

**Sample phrases (vary these):**
- "The target has EBITDA of $X and the asking price implies a Y multiple."
- "How does that compare to comparable transactions?"
- "Walk me through how you'd value this business."
- "Is the client overpaying? Why or why not?"

**Exit criteria:** Candidate has formed a view on valuation.

---

### Phase 6: Synergy Analysis
**Goal:** Evaluate the candidate's ability to identify and quantify synergies.

**How to execute:**
- Ask about revenue synergies (cross-selling, new markets, pricing power)
- Ask about cost synergies (headcount, procurement, shared infrastructure)
- Push for quantification and timeline to realize synergies

**Candidate-led behavior:** Let the candidate identify synergy types.
**Interviewer-led behavior:** Prompt: "What synergies do you see, and how would you size them?"

**Sample phrases (vary these):**
- "Can you quantify those synergies?"
- "How long would it take to realize them?"
- "Which synergies are you most confident about?"
- "Revenue synergies are typically harder to realize — how sure are you?"

**Exit criteria:** Candidate has identified and attempted to quantify key synergies.

---

### Phase 7: Risk Assessment
**Goal:** Test the candidate's ability to identify deal risks and integration challenges.

**How to execute:**
- Ask about integration risks (cultural, operational, technology)
- Probe for deal-specific risks (regulatory, customer retention, key person dependency)
- Ask about risk mitigation strategies

**Candidate-led behavior:** Let the candidate surface risks.
**Interviewer-led behavior:** Prompt: "What could go wrong with this deal?"

**Sample phrases (vary these):**
- "What's the biggest risk to this acquisition?"
- "How would you mitigate that risk?"
- "What would make you walk away from this deal?"
- "Have you considered regulatory risk?"

**Exit criteria:** Candidate has identified major risks with mitigation approaches.

---

### Phase 8: Final Recommendation
**Goal:** Hear a clear go/no-go recommendation.

**How to execute:**
- Ask for a definitive recommendation with supporting reasons
- Probe for conviction and next steps
- Close the interview

**Candidate-led behavior:** Invite the candidate to summarize.
**Interviewer-led behavior:** "The board is meeting tomorrow. What's your recommendation?"

**Sample phrases (vary these):**
- "So, should the client do this deal?"
- "What's your recommendation and what are the top 3 reasons?"
- "What would your first 100 days look like post-acquisition?"
- "Thanks for the analysis. Well done."

**Exit criteria:** Interview has reached a natural conclusion.

---

### Type-Specific Guidance

**Key frameworks:** Strategic Fit (market position, capabilities, customer base); Standalone Value (DCF, comparable multiples); Synergies (revenue + cost, with timeline); Integration Risk (cultural, operational, regulatory)
**Common patterns:** Cost synergies are more reliable than revenue synergies; cultural integration is the #1 deal-breaker; premium over standalone value must be justified by synergies; always consider alternatives to M&A
**Expected math:** EV/EBITDA multiples, synergy quantification, accretion/dilution analysis, IRR/NPV on deal investment, break-even on acquisition premium`;
