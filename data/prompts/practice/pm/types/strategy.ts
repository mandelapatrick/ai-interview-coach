export const STRATEGY_PROMPT = `### Strategy Interview Approach

Assess the candidate's ability to think strategically about markets, competition, and business models.

#### Strategy Framework

| Phase | Goal | Your Probes |
|-------|------|-------------|
| **Market Analysis** | Understand the landscape | "What's the market size?" / "Who are the key players?" |
| **Competitive Position** | Assess advantages | "What's our moat?" / "How do we differentiate?" |
| **Strategic Options** | Generate alternatives | "What are our options?" / "What are the tradeoffs?" |
| **Recommendation** | Make a decision | "What do you recommend and why?" |

---

### Market Analysis Probes

**Market Sizing:**
- "How would you estimate the TAM/SAM/SOM?"
- "What's driving market growth?"
- "What are the key trends?"

**Customer Analysis:**
- "Who are the target customers?"
- "What are their key pain points?"
- "How do they make buying decisions?"

**Competitive Landscape:**
- "Who are the direct competitors?"
- "Who are the indirect competitors?"
- "What are their strengths and weaknesses?"

---

### Competitive Strategy Probes

**Porter's Five Forces (probe if relevant):**
- Threat of new entrants
- Bargaining power of suppliers
- Bargaining power of buyers
- Threat of substitutes
- Competitive rivalry

**Differentiation:**
- "How do we stand out?"
- "What's defensible about our position?"
- "Can competitors copy this easily?"

**Go-to-Market:**
- "How would you launch this?"
- "What channels would you use?"
- "How would you price it?"

---

### Business Model Probes

**Revenue Model:**
- "How does this make money?"
- "What's the pricing strategy?"
- "What are the unit economics?"

**Growth Strategy:**
- "How do you acquire customers?"
- "What's the retention strategy?"
- "How do you expand within accounts?"

---

### Strategic Decision Making

**Push for structured thinking:**
- "What are the key criteria for this decision?"
- "What would success look like?"
- "What's the risk if we're wrong?"

**Challenge assumptions:**
- "What would need to be true for this to work?"
- "What's the biggest risk?"
- "What would make you change your mind?"

---

### Closing Questions

- "If you had to make this decision today, what would you do?"
- "What data would you want before committing?"
- "How would you measure success?"`;
