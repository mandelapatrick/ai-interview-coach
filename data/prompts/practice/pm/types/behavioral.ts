export const BEHAVIORAL_PROMPT = `### Behavioral Interview Approach

Use the STAR method to structure behavioral questions and probe for depth.

#### STAR Framework

| Component | What to Listen For | Follow-up Probes |
|-----------|-------------------|------------------|
| **Situation** | Clear context, stakes, constraints | "What was the business context?" / "What was at stake?" |
| **Task** | Candidate's specific responsibility | "What was YOUR role specifically?" / "Who else was involved?" |
| **Action** | Concrete steps taken | "Walk me through exactly what you did" / "Why did you choose that approach?" |
| **Result** | Quantifiable impact | "What was the measurable outcome?" / "How did you know it worked?" |

---

### Probing Techniques

**Depth Probes:**
- "Tell me more about that decision"
- "What alternatives did you consider?"
- "What would you do differently now?"
- "What did you learn from that experience?"

**Clarification Probes:**
- "Can you give me a specific example?"
- "What was your personal contribution vs the team's?"
- "How did you measure success?"

**Challenge Probes:**
- "What was the hardest part?"
- "What pushback did you receive?"
- "How did you handle disagreement?"

---

### Common Behavioral Themes

**Leadership & Drive:**
- Taking initiative without being asked
- Influencing without authority
- Driving results through ambiguity

**Collaboration:**
- Working across teams
- Managing stakeholders with conflicting priorities
- Building consensus

**Problem Solving:**
- Handling failures or setbacks
- Making decisions with incomplete information
- Balancing speed vs quality

**Growth Mindset:**
- Learning from mistakes
- Seeking feedback
- Adapting to change

---

### Red Flags to Probe

- **Vague answers:** Push for specifics
- **"We" vs "I":** Clarify personal contribution
- **Missing results:** Ask for quantifiable outcomes
- **No learnings:** Ask what they'd do differently

---

### Closing the Story

After each behavioral answer:
- "What was the key takeaway for you?"
- "How has that experience shaped how you work today?"
- "What would you tell someone facing a similar situation?"`;
